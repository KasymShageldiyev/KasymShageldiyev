import numpy as np

# Определяем нормальный вектор плоскости
n = np.array([1, -2, 1])

# Определяем базисные векторы в плоскости
v1 = np.array([2, 1, 0])
v2 = np.array([1, 0, 1])

# Определяем действие оператора фи на базисные векторы
# Для v1 и v2 действие тождественное, для n умножаем на 2
phi_v1 = v1
phi_v2 = v2
phi_n = 2 * n

# Составляем матрицу оператора фи
# Каждый столбец - это образ базисного вектора после применения оператора
phi_matrix = np.column_stack((phi_v1, phi_v2, phi_n))

print("Матрица оператора фи:")
print(phi_matrix)
